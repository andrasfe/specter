# Response parsing (unchanged from v1)
# ---------------------------------------------------------------------------

def parse_test_states(response_text: str) -> list[LLMTestState]:
    """Parse LLM JSON response into LLMTestState objects.

    Handles markdown code fences, truncated responses (partial JSON recovery).
    """
    text = response_text.strip()

    # Strip markdown code fences
    if "```" in text:
        parts = text.split("```")
        for part in parts[1::2]:
            lines = part.strip().split("\n", 1)
            if len(lines) > 1 and lines[0].strip().lower() in ("json", ""):
                text = lines[1]
            else:
                text = part
            break

    # Try direct parse
    data = None
    try:
        data = json.loads(text)
    except json.JSONDecodeError:
        pass

    # Try to find JSON array
    if data is None:
        start = text.find("[")
        end = text.rfind("]")
        if start >= 0 and end > start:
            try:
                data = json.loads(text[start:end + 1])
            except json.JSONDecodeError:
                # Truncated response — try to recover partial array
                data = _recover_partial_json(text[start:])

    if data is None:
        log.warning("Failed to parse LLM response as JSON")
        return []

    if not isinstance(data, list):
        data = [data]

    states = []
    for item in data:
        if not isinstance(item, dict):
            continue
        state = LLMTestState(
            input_values={
                str(k): str(v)
                for k, v in item.get("input_values", {}).items()
                if isinstance(k, str)
            },
            stub_overrides={
                str(k): str(v)
                for k, v in item.get("stub_overrides", {}).items()
                if isinstance(k, str)
            },
            target_description=str(item.get("target", "")),
            reasoning=str(item.get("reasoning", "")),
        )
        if state.input_values or state.stub_overrides:
            states.append(state)

    return states


def _recover_partial_json(text: str) -> list | None:
    """Try to recover a partial JSON array from truncated LLM output."""
    results = []
    depth = 0
    obj_start = None
    in_string = False
    escape = False

    for i, ch in enumerate(text):
        if escape:
            escape = False
            continue
        if ch == '\\' and in_string:
            escape = True
            continue
        if ch == '"':
            in_string = not in_string
            continue
        if in_string:
            continue

        if ch == '{':
            if depth == 0:
                obj_start = i
            depth += 1
        elif ch == '}':
            depth -= 1
            if depth == 0 and obj_start is not None:
                try:
                    obj = json.loads(text[obj_start:i + 1])
                    results.append(obj)
                except json.JSONDecodeError:
                    pass
                obj_start = None

    return results if results else None


