def _compute_cache_key(cobol_source: str, var_names: list[str]) -> str:
    """Cache key = SHA-256 of source content + sorted variable names."""
    payload = cobol_source + "\n" + "\n".join(sorted(var_names))
    return hashlib.sha256(payload.encode()).hexdigest()[:16]


def generate_llm_test_states(
    provider,
    cobol_source_path: Path,
    program: Program,
    var_report: VariableReport,
    stub_mapping: dict[str, list[str]],
    domains: dict[str, VariableDomain],
    cache_path: Path,
    call_graph: StaticCallGraph | None = None,
    gating_conds: dict[str, list] | None = None,
    model: str | None = None,
    covered_paragraphs: set[str] | None = None,
    covered_branches: set[str] | None = None,
) -> list[LLMTestState]:
    """Orchestrate the 3-agent pipeline to generate LLM test states.

    1. Read COBOL source lines
    2. Run Comments Agent → condensed paragraph annotations
    3. Run Flow Agent → compact flow summary (needs call_graph + gating_conds)
    4. Run Synthesis Agent → prompt → LLM query → parse → list[LLMTestState]
    5. Cache + return
    """
    cobol_source_path = Path(cobol_source_path)
    cache_path = Path(cache_path)

    cobol_text = cobol_source_path.read_text(errors="replace")
    source_lines = cobol_text.splitlines()
    var_names = sorted(var_report.variables.keys())
    cache_key = _compute_cache_key(cobol_text, var_names)

    # Check cache (only for initial call without coverage gaps)
    is_gap_query = covered_paragraphs is not None and len(covered_paragraphs) > 0
    if cache_path.exists() and not is_gap_query:
        try:
            cached = json.loads(cache_path.read_text())
            if cached.get("_cache_key") == cache_key:
                states = [
                    LLMTestState(
                        input_values=entry.get("input_values", {}),
                        stub_overrides=entry.get("stub_overrides", {}),
                        target_description=entry.get("target", ""),
                        reasoning=entry.get("reasoning", ""),
                    )
                    for entry in cached.get("states", [])
                ]
                log.info("LLM test states loaded from cache: %d states", len(states))
                return states
        except Exception as e:
            log.warning("Cache read failed: %s", e)

    # Agent 1: Comments Agent
    comments = extract_paragraph_comments(program, source_lines)
    log.info("Comments agent: %d paragraphs annotated", len(comments))

    # Agent 2: Flow Agent
    if call_graph is not None:
        flow_summary = extract_flow_summary(
            program, call_graph, stub_mapping, gating_conds or {},
        )
    else:
        # Fallback: simple paragraph list with PERFORM targets
        flow_summary = _fallback_flow_summary(program)
    log.info("Flow agent: %d flow lines", len(flow_summary))

    # Agent 3: Synthesis Agent — build prompt and query LLM
    prompt = build_synthesis_prompt(
        program.program_id, comments, flow_summary,
        var_report, stub_mapping, domains,
        covered_paragraphs, covered_branches,
    )

    if not prompt:
        log.info("No content for LLM query")
        return []

    log.info("Querying LLM for test states (%d paragraphs, %d vars, prompt %d chars) ...",
             len(program.paragraphs), len(var_names), len(prompt))

    try:
        response_text, tokens = _query_llm_sync(provider, prompt, model)
        log.info("LLM response: %d chars, %d tokens", len(response_text), tokens)
    except Exception as e:
        log.error("LLM query failed: %s", e)
        return []

    states = parse_test_states(response_text)
    log.info("Parsed %d test states from LLM response", len(states))

    # Write cache (only for initial queries)
    if states and not is_gap_query:
        try:
            cache_data = {
                "_cache_key": cache_key,
                "_source_file": str(cobol_source_path),
                "states": [
                    {
                        "input_values": s.input_values,
                        "stub_overrides": s.stub_overrides,
                        "target": s.target_description,
                        "reasoning": s.reasoning,
                    }
                    for s in states
                ],
            }
            cache_path.parent.mkdir(parents=True, exist_ok=True)
            cache_path.write_text(json.dumps(cache_data, indent=2, default=str))
            log.info("LLM test states cached to %s", cache_path)
        except Exception as e:
            log.warning("Failed to write cache: %s", e)

    return states


def _fallback_flow_summary(program: Program) -> list[str]:
    """Simple flow summary when call_graph is not available."""
    lines: list[str] = []
    for para in program.paragraphs[:60]:
        performs = []
        for stmt in para.statements:
            for s in stmt.walk():
                if s.type == "PERFORM" and s.text:
                    target = s.text.split()[0] if s.text.split() else ""
                    if target and target != para.name:
                        performs.append(target)
        if performs:
            lines.append(f"{para.name}: calls [{', '.join(performs[:5])}]")
        else:
            lines.append(para.name)
    return lines
