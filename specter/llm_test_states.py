"""LLM-driven test state generation for COBOL coverage.

Uses a 3-agent pipeline to build a compact, high-signal prompt:
  1. Comments Agent (deterministic) — extracts business-logic comments from
     COBOL source between paragraph boundaries.
  2. Flow Agent (deterministic) — summarises call graph, stub operations,
     gating conditions, and branch structure from the AST.
  3. Synthesis Agent (LLM) — receives the compact context and queries the
     LLM for test states.

Designed to seed cobol_coverage.py Layer 0 with high-quality initial states
that target stub-dependent branches (CICS DFHRESP, IMS DLI status, file
status codes) which heuristic generation cannot reach.
"""

from __future__ import annotations

import hashlib
import json
import logging
import re
from dataclasses import dataclass, field
from pathlib import Path

from .llm_coverage import _query_llm_sync
from .models import Program, Paragraph, Statement
from .static_analysis import StaticCallGraph, GatingCondition
from .variable_domain import VariableDomain
from .variable_extractor import VariableReport

log = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Data structures
# ---------------------------------------------------------------------------

@dataclass
class LLMTestState:
    """A complete test state generated by the LLM."""

    input_values: dict[str, str] = field(default_factory=dict)
    stub_overrides: dict[str, str] = field(default_factory=dict)
    target_description: str = ""
    reasoning: str = ""


# ---------------------------------------------------------------------------
# Agent 1: Comments Agent (deterministic)
# ---------------------------------------------------------------------------

_DECORATIVE_RE = re.compile(r'^[\s*=\-/\\#+]+$')
_BOILERPLATE_KW = frozenset({
    "copyright", "license", "licensed", "all rights reserved",
    "modification log", "change log", "change history",
    "author:", "date:", "written by",
})


def extract_paragraph_comments(
    program: Program,
    source_lines: list[str],
) -> dict[str, list[str]]:
    """Extract condensed comment bullets for each paragraph.

    Looks at COBOL comment lines in the zone between the previous
    paragraph's end and the current paragraph's start.
    """
    if not program.paragraphs or not source_lines:
        return {}

    sorted_paras = sorted(program.paragraphs, key=lambda p: p.line_start)
    result: dict[str, list[str]] = {}

    for idx, para in enumerate(sorted_paras):
        # Comment zone: from previous paragraph end (or line 0) to this start
        if idx == 0:
            zone_start = 0
        else:
            zone_start = sorted_paras[idx - 1].line_end  # 1-based end → 0-based next

        zone_end = para.line_start - 1  # 1-based start → 0-based line before

        if zone_end <= zone_start:
            continue

        raw_comments = _collect_comment_lines(source_lines, zone_start, zone_end)
        bullets = _compact_comments(raw_comments)
        if bullets:
            result[para.name] = bullets

    return result


def _collect_comment_lines(
    source_lines: list[str],
    zone_start: int,
    zone_end: int,
) -> list[str]:
    """Collect comment text from source lines in the given 0-based range."""
    comments: list[str] = []

    for i in range(zone_start, min(zone_end, len(source_lines))):
        line = source_lines[i]

        # Fixed-format: column 7 (index 6) is '*' → rest is comment
        if len(line) > 6 and line[6] == '*':
            text = line[7:].strip()
            if text and line[6:8] != '*>':
                comments.append(text)
            continue

        # Inline comment: *> anywhere
        if '*>' in line:
            text = line.split('*>', 1)[1].strip()
            if text:
                comments.append(text)

    return comments


def _compact_comments(raw: list[str]) -> list[str]:
    """Filter decorative/boilerplate lines and compact into bullet points."""
    filtered: list[str] = []

    for line in raw:
        # Skip decorative
        if _DECORATIVE_RE.match(line):
            continue
        # Skip boilerplate
        lower = line.lower()
        if any(kw in lower for kw in _BOILERPLATE_KW):
            continue
        # Skip very short noise
        if len(line) < 3:
            continue
        filtered.append(line)

    if not filtered:
        return []

    # Collapse multi-line sentences and deduplicate
    bullets: list[str] = []
    current = filtered[0]
    seen: set[str] = set()

    for line in filtered[1:]:
        # If line looks like a continuation (starts lowercase, no bullet marker)
        if line[0].islower() and not line.startswith(('-', '*', '•')):
            current = current.rstrip('.') + ' ' + line
        else:
            norm = current.strip().lower()
            if norm not in seen:
                seen.add(norm)
                bullets.append(current.strip())
            current = line

    # Flush last
    norm = current.strip().lower()
    if norm not in seen:
        bullets.append(current.strip())

    return bullets[:5]  # Max 5 bullets per paragraph


# ---------------------------------------------------------------------------
# Agent 2: Flow Agent (deterministic)
# ---------------------------------------------------------------------------

def extract_flow_summary(
    program: Program,
    call_graph: StaticCallGraph,
    stub_mapping: dict[str, list[str]],
    gating_conds: dict[str, list],
) -> list[str]:
    """Produce a compact per-paragraph flow summary.

    Format: PARA-NAME: calls [A, B]; stubs [READ:FILE]; gated by [VAR = 'X']; branches [IF cond1]
    """
    lines: list[str] = []
    # Build reverse stub lookup: paragraph → list of op_keys
    para_stubs = _build_paragraph_stub_map(program, stub_mapping)

    for para in program.paragraphs[:60]:
        parts: list[str] = []

        # Call targets from call_graph edges
        targets = call_graph.edges.get(para.name, set())
        if targets:
            parts.append(f"calls [{', '.join(sorted(targets)[:5])}]")

        # Stub operations
        ops = para_stubs.get(para.name, [])
        if ops:
            parts.append(f"stubs [{', '.join(ops[:4])}]")

        # Gating conditions
        gates = gating_conds.get(para.name, [])
        if gates:
            gate_strs = []
            for gc in gates[:3]:
                if hasattr(gc, 'variable') and hasattr(gc, 'values'):
                    neg = "NOT " if gc.negated else ""
                    vals = ', '.join(repr(v) for v in gc.values[:3])
                    gate_strs.append(f"{neg}{gc.variable} = {vals}")
            if gate_strs:
                parts.append(f"gated by [{'; '.join(gate_strs)}]")

        # Branch conditions (from statement tree)
        branches = _extract_branch_conditions(para)
        if branches:
            parts.append(f"branches [{', '.join(branches[:3])}]")

        if parts:
            lines.append(f"{para.name}: {'; '.join(parts)}")
        else:
            lines.append(para.name)

    return lines


def _build_paragraph_stub_map(
    program: Program,
    stub_mapping: dict[str, list[str]],
) -> dict[str, list[str]]:
    """Map each paragraph to the stub operation keys it references."""
    # Build a set of all op prefixes for matching
    op_prefixes: dict[str, str] = {}
    for op_key in stub_mapping:
        # e.g. "READ:XREF-FILE" → type=READ, target=XREF-FILE
        if ':' in op_key:
            op_type, op_target = op_key.split(':', 1)
            op_prefixes[op_target.upper()] = op_key
            op_prefixes[op_type.upper()] = op_key
        else:
            op_prefixes[op_key.upper()] = op_key

    result: dict[str, list[str]] = {}

    for para in program.paragraphs:
        ops: list[str] = []
        seen: set[str] = set()
        for stmt in para.statements:
            for s in stmt.walk():
                matched = _match_stmt_to_stub(s, stub_mapping, op_prefixes)
                if matched and matched not in seen:
                    seen.add(matched)
                    ops.append(matched)
        if ops:
            result[para.name] = ops

    return result


def _match_stmt_to_stub(
    stmt: Statement,
    stub_mapping: dict[str, list[str]],
    op_prefixes: dict[str, str],
) -> str | None:
    """Try to match a statement to a stub_mapping key."""
    text_upper = stmt.text.upper() if stmt.text else ""
    stype = stmt.type.upper()

    # Direct type-based: READ, WRITE, REWRITE, DELETE
    if stype in ("READ", "WRITE", "REWRITE", "DELETE", "START",
                  "STARTBR", "READNEXT", "READPREV"):
        # Try to extract filename from text
        words = text_upper.split()
        for word in words:
            key = f"{stype}:{word}"
            if key in stub_mapping:
                return key
            if word in op_prefixes:
                return op_prefixes[word]
        # Fallback: just the type
        for k in stub_mapping:
            if k.startswith(stype + ':') or k == stype:
                return k

    # EXEC blocks
    if stype == "EXEC" or "EXEC CICS" in text_upper or "EXEC DLI" in text_upper:
        if "EXEC DLI" in text_upper or "DLI" in text_upper:
            for k in stub_mapping:
                if k.startswith("DLI"):
                    return k
        if "EXEC CICS" in text_upper:
            # Try to extract CICS command
            m = re.search(r'EXEC\s+CICS\s+(\w+)', text_upper)
            if m:
                cmd = m.group(1)
                for k in stub_mapping:
                    if cmd in k.upper():
                        return k

    # CALL statements
    if stype == "CALL" or "CALL " in text_upper:
        for k in stub_mapping:
            if k.startswith("CALL:"):
                target = k.split(':', 1)[1].upper()
                if target in text_upper:
                    return k

    return None


def _extract_branch_conditions(para: Paragraph) -> list[str]:
    """Extract short condition descriptions from IF/EVALUATE statements."""
    conditions: list[str] = []

    for stmt in para.statements:
        for s in stmt.walk():
            if s.type == "IF" and s.text:
                cond = s.text.strip()
                # Truncate long conditions
                if len(cond) > 60:
                    cond = cond[:57] + "..."
                conditions.append(f"IF {cond}")
            elif s.type == "EVALUATE" and s.text:
                cond = s.text.strip()
                if len(cond) > 40:
                    cond = cond[:37] + "..."
                conditions.append(f"EVALUATE {cond}")

    return conditions


# ---------------------------------------------------------------------------
# Agent 3: Synthesis Agent (LLM-powered)
# ---------------------------------------------------------------------------

def build_synthesis_prompt(
    program_id: str,
    comments: dict[str, list[str]],
    flow_summary: list[str],
    var_report: VariableReport,
    stub_mapping: dict[str, list[str]],
    domains: dict[str, VariableDomain],
    covered_paragraphs: set[str] | None = None,
    covered_branches: set[str] | None = None,
) -> str:
    """Build a compact prompt from agent outputs — no raw COBOL source."""

    # Flow section
    flow_block = "\n".join(flow_summary) if flow_summary else "(empty program)"

    # Paragraph annotations
    anno_lines = []
    for para_name, bullets in sorted(comments.items()):
        anno_lines.append(f"{para_name}: {'; '.join(bullets)}")
    anno_block = "\n".join(anno_lines[:50]) if anno_lines else "(no comments)"

    # Variable summary
    var_lines = []
    for name, dom in sorted(domains.items()):
        if dom.classification not in ("input", "status", "flag"):
            continue
        if dom.set_by_stub:
            continue
        extras = []
        if dom.condition_literals:
            extras.append(f"known_values={dom.condition_literals[:8]!r}")
        if dom.valid_88_values:
            extras.append(f"88_levels={dict(list(dom.valid_88_values.items())[:6])!r}")
        if dom.semantic_type != "generic":
            extras.append(f"type={dom.semantic_type}")
        if dom.max_length and dom.max_length != 10:
            extras.append(f"len={dom.max_length}")
        extra_str = f" ({', '.join(extras)})" if extras else ""
        var_lines.append(f"  {name}: {dom.classification}{extra_str}")

    var_summary = "\n".join(var_lines[:60]) if var_lines else "  (none)"
    if len(var_lines) > 60:
        var_summary += f"\n  ... and {len(var_lines) - 60} more"

    # Stub mapping summary
    stub_lines = []
    for op_key, status_vars in sorted(stub_mapping.items()):
        status_info = []
        for sv in status_vars:
            dom = domains.get(sv)
            if dom:
                vals = []
                if dom.condition_literals:
                    vals.extend(str(v) for v in dom.condition_literals[:6])
                if dom.valid_88_values:
                    for label, val in list(dom.valid_88_values.items())[:4]:
                        vals.append(f"{val}={label}")
                if vals:
                    status_info.append(f"{sv} [{', '.join(vals)}]")
                else:
                    status_info.append(sv)
            else:
                status_info.append(sv)
        stub_lines.append(f"  {op_key} -> {'; '.join(status_info)}")

    stub_summary = "\n".join(stub_lines[:40]) if stub_lines else "  (none)"

    # Coverage gaps
    coverage_section = ""
    if covered_paragraphs is not None:
        coverage_section += f"\n=== COVERAGE GAPS ===\n"
        coverage_section += f"Covered: {len(covered_paragraphs)} paragraphs\n"
        # Show uncovered paragraphs from flow_summary
        all_para_names = set()
        for line in flow_summary:
            name = line.split(':')[0].strip() if ':' in line else line.strip()
            if name:
                all_para_names.add(name)
        uncovered = sorted(all_para_names - covered_paragraphs)
        if uncovered:
            shown = uncovered[:30]
            coverage_section += f"Uncovered paragraphs: {', '.join(shown)}"
            if len(uncovered) > 30:
                coverage_section += f" ... and {len(uncovered) - 30} more"
            coverage_section += "\n"
        coverage_section += "\nFocus on reaching UNCOVERED paragraphs.\n"

    if covered_branches is not None:
        coverage_section += f"Already covered branches: {len(covered_branches)}\n"
        coverage_section += "Target branch directions not yet covered.\n"

    prompt = f"""\
You are an expert COBOL test engineer. Analyze this program's structure and
generate test states that maximize code coverage, especially targeting
error-handling paths, EVALUATE branches, and stub-dependent control flow.

=== PROGRAM: {program_id} ===

=== FLOW ===
{flow_block}

=== PARAGRAPH ANNOTATIONS ===
{anno_block}

=== INPUT VARIABLES ===
{var_summary}

=== STUB OPERATIONS ===
{stub_summary}
{coverage_section}
=== INSTRUCTIONS ===
Generate 5-10 test states, each targeting a DIFFERENT execution path through
the program.
Each test state must include:
- input_values: variable values to set before execution
- stub_overrides: operation status overrides (e.g., "READ:MYFILE" -> "23" for not-found)
- target: which paragraph or path this state is trying to reach
- reasoning: brief explanation of why these values trigger that path

For stub_overrides, use the operation keys shown above (e.g., "READ:filename",
"DLI", "EXEC:CICS:SEND", etc.) and set them to status values that trigger
specific error-handling paths:
- File status: "00"=success, "10"=EOF, "23"=not-found, "35"=not-exists
- DLI PCB: "  "=success, "GE"=not-found, "GB"=end, "II"=duplicate
- CICS EIBRESP: 0=normal, 12=not-found, 13=dup-record, 16=invreq, 22=lenerr, 27=notfnd
- SQL SQLCODE: 0=success, 100=not-found, -803=duplicate, -805=not-found

IMPORTANT: Values must be strings matching COBOL field sizes.
Respond with ONLY a JSON array, no other text:
[
  {{
    "input_values": {{"VARIABLE-NAME": "value", ...}},
    "stub_overrides": {{"OPERATION:KEY": "status", ...}},
    "target": "paragraph or path description",
    "reasoning": "brief explanation"
  }}
]"""

    return prompt


